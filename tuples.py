# 5) WAP to create a tuple and demonstrate any four tuple methods.

tup = (10,20,30,40,50,60,70,80)
print("\n\n The Given Tuple is : ",tup)
 
x = tup.index(30)
print("\n\n Index Number of Element 30 is : ",x)

x = max(tup)
print("\n\n Largest Element in Tuple is : ",x)

x = min(tup)
print("\n\n Smallest Element in Tuple is : ",x)

x = len(tup)
print("\n\n The Length of Tuple is : ",x)

 