# 6) WAP to create a set and demonstrate any four set methods.

set = {10,20,30,40,50}
print("\n\n The Given Set is : ",set)

set.add(60)
print("\n\n After adding elements set is : ",set)

set.discard(40)
print("\n\n After discard elements set is : ",set)

set.remove(20)
print("\n\n After remove elements set is : ",set)

x = set.pop()
print("\n\n After pop elements set is : ",x)

x = set.clear()
print("\n\n After clearing elements set is : ",x)